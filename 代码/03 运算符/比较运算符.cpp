#include<iostream>
using namespace std;

int main() {
	//比较运算，判断真假，输出0或1
	int a = 4;
	int b = 56;
	cout << (a == b) << endl;
	cout << (a != b) << endl;
	cout << (a < b) << endl;
	cout << (a > b) << endl;
	cout << (a <= b) << endl;
	cout << (a >= b) << endl;
	
	system("pause");

	return 0;
}